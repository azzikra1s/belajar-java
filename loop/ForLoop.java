public class ForLoop {
    public static void main(String[] args) {
        /*
            * Struktur For Loop
            * for (init statement; kondisi; increment/decrement) {
            *    // blok kode yang akan diulang
            * }
        */
        
        for (int i = 1; i <= 5; i++) {
            System.out.println("Perulangan ke-" + i);
        }
    }
}